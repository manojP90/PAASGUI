package com.getusroi.paas.freenas;

public class FreeNasFlowManagerException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public FreeNasFlowManagerException() {
		super();
	}

	public FreeNasFlowManagerException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public FreeNasFlowManagerException(String message, Throwable cause) {
		super(message, cause);
	}

	public FreeNasFlowManagerException(String message) {
		super(message);
	}

	public FreeNasFlowManagerException(Throwable cause) {
		super(cause);
	}

}
